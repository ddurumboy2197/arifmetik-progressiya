def arifmetik_progressiya(n, a, d):
    return [a + i * d for i in range(n)]

# Misol:
n = 10  # Progressiyaning uzunligi
a = 2   # Progressiyaning birinchi termi
d = 3   # Progressiyaning farqi

print(arifmetik_progressiya(n, a, d))
